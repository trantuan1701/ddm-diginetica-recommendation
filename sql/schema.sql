    ON fact_recommendation_eval(model_key);

CREATE INDEX ix_fact_recommendation_eval_session_id
    ON fact_recommendation_eval(session_id);

CREATE INDEX ix_fact_recommendation_eval_item_id
    ON fact_recommendation_eval(item_id);

CREATE INDEX ix_fact_marketing_kpis_model_key
    ON fact_marketing_kpis(model_key);

CREATE INDEX ix_fact_marketing_kpis_kpi_name
    ON fact_marketing_kpis(kpi_name);

CREATE INDEX ix_session_summary_timeframe
    ON session_summary(timeframe);
